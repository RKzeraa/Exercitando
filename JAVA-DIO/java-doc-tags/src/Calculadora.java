public class Calculadora {
    /**
     * Este método é utilizadao para somar dois números inteiros
     * @param numberOne este é o primeiro parâmetro do método
     * @param numberTwo este é o segundo parâmetro do método
     * @return int o resultado deste método é a soma dos dois números
     */
    public int somar(int numberOne, int numberTwo) {
        return numberOne + numberTwo;
    }
}